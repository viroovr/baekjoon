def get_max_number():
    n = tuple(str(N))
    M = len(n)
    k_numbers = {n}

    for _ in range(K):
        new_numbers = set()
        for k in k_numbers:
            k_list = list(k)
            for i in range(M):
                for j in range(i + 1, M):
                    if i == 0 and k_list[j] == '0':
                        continue
                    k_list[i], k_list[j] = k_list[j], k_list[i]
                    new_numbers.add(tuple(k_list))
                    k_list[i], k_list[j] = k_list[j], k_list[i]
        if not new_numbers:
            print(-1)
            return
        k_numbers = new_numbers
    
    print("".join(max(k_numbers)))
    
N, K = map(int, input().split())
get_max_number()