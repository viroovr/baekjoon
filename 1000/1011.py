for _ in range(T):
    x, y = map(int, input().split())
    ans = -1
    d = y - x
    a = (int(d ** (1/2))) ** 2
    b = (int(d ** (1/2)) + 1) ** 2
    if a < d <= b:
        k = (b - a) // 2
        if d <= a + k:
            ans = (b ** (1/2)) * 2 - 2
        else:
            ans = (b ** (1/2)) * 2 - 1
    elif d == a:
        ans = (a ** (1/2)) * 2 - 1
    # print(f"ans : {int(ans)}")
    print(int(ans))
